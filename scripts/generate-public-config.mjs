function buildPrettyAmounts() {
  const amounts = [];
  for (let exp = 0; exp <= 18; exp++) {
    const base = 10 ** exp;
    for (let digit = 1; digit <= 9; digit++) {
      amounts.push(digit * base);
    }
  }
  return amounts;
}

